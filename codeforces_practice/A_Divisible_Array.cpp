#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin>>t;
    while (t--)
    {
        int count;
        cin>>count;
        
        
        for (int i = 1; i <= count; i++)
        { 
                cout<<2*i<<" ";                           
        }
        cout<<endl;
        
    }
    
}